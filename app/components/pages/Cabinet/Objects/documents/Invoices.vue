<script setup>
import { ref, computed } from 'vue'
import PagesCabinetUiModal from '@/components/pages/cabinet/ui/Modal.vue'
import Card from '@/components/pages/cabinet/ui/cards/card.vue'

const props = defineProps({
  objectId: {
    type: Number,
    required: true
  },
  invoices: {
    type: Array,
    default: () => []
  },
  isAdmin: {
    type: Boolean,
    default: false
  }
})

const emit = defineEmits(['add', 'update', 'delete'])

// Локальные данные
const items = ref(props.invoices)
const isModalOpen = ref(false)
const editing = ref(null)
const form = ref({ name: '', amount: '', comment: '', subtype: '' })

// Тексты
const statusText = {
  prepared: 'Подготовлен',
  sent: 'Отправлен',
  paid: 'Оплачен'
}

const subtypeText = {
  advance: 'Аванс',
  intermediate: 'Промежуточный',
  final: 'Закрывающий',
  additional: 'Дополнительный'
}

// --- Открытие модалки ---
function openModal(item = null) {
  editing.value = item
  if (item) {
    form.value = { ...item }
  } else {
    form.value = { name: '', amount: '', comment: '', subtype: '' }
  }
  isModalOpen.value = true
}

// --- Сохранение ---
async function save() {
  const { name, amount, comment, subtype } = form.value
  if (!name || amount == null || amount < 0) {
    alert('Введите корректные название и сумму')
    return
  }

  try {
    let result
    if (editing.value) {
      // Обновляем
      result = await $fetch(`/api/objects/invoices/${editing.value.id}`, {
        method: 'PUT',
        body: { name, amount, comment, subtype },
        credentials: 'include'
      })
      const index = items.value.findIndex(i => i.id === editing.value.id)
      if (index !== -1) {
        items.value.splice(index, 1, result)
      }
      emit('update', result)
    } else {
      // Создаём
      result = await $fetch(`/api/objects/${props.objectId}/invoices`, {
        method: 'POST',
        body: { name, amount, comment, subtype },
        credentials: 'include'
      })
      items.value.push(result)
      emit('add', result)
    }

    isModalOpen.value = false
  } catch (error) {
    console.error('Ошибка сохранения счёта:', error)
    alert('Не удалось сохранить счёт')
  }
}

// --- Смена статуса ---
async function setStatus(item, newStatus) {
  try {
    const updated = await $fetch(`/api/objects/invoices/${item.id}`, {
      method: 'PUT',
      body: { status: newStatus },
      credentials: 'include'
    })

    const index = items.value.findIndex(i => i.id === item.id)
    if (index !== -1) {
      items.value.splice(index, 1, updated)
    }

    emit('update', updated)
  } catch (error) {
    console.error('Ошибка изменения статуса:', error)
    alert('Не удалось изменить статус счёта')
  }
}

// --- Удаление ---
function confirmDelete(id) {
  if (window.confirm('Удалить этот счёт?')) {
    deleteItem(id)
  }
}

async function deleteItem(id) {
  try {
    await $fetch(`/api/objects/invoices/${id}`, {
      method: 'DELETE',
      credentials: 'include'
    })

    items.value = items.value.filter(i => i.id !== id)
    emit('delete', id)
  } catch (error) {
    console.error('Ошибка удаления:', error)
    alert('Не удалось удалить счёт')
  }
}

// --- Утилиты ---
const formatCurrency = (value) => {
  return new Intl.NumberFormat('ru-RU').format(value || 0) + ' ₽'
}
</script>

