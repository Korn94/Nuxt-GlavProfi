          ></textarea>
        </div>
      </form>

      <template #footer>
        <div class="modal-footer-controls">
          <button type="button" @click="isModalOpen = false" class="btn btn-secondary">Отмена</button>
          <button type="submit" @click="save" class="btn btn-primary">
            {{ editing ? 'Сохранить' : 'Добавить' }}
          </button>
        </div>
      </template>
    </PagesCabinetUiModal>
  </Card>
