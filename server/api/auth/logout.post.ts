/**
 * 📍 Эндпоинт: POST /api/auth/logout
 * 
 * Назначение:
 * - Завершение пользовательской сессии при выходе из системы
 * - Очистка серверной сессии в таблице `userSessions`
 * - Возврат успеха даже при ошибках — чтобы клиент мог корректно очистить куки
 * 
 * ⚠️ ВАЖНО: Эндпоинт находится в `PUBLIC_PATHS` мидлвара.
 * Он НЕ требует валидного токена — это позволяет разлогиниться даже при истёкшем токене.
 * 
 * @body {} (пустое тело, авторизация через куки)
 * @returns { success: boolean } — всегда true для гарантированной очистки на клиенте
 * 
 * Примеры ответов:
 * ✅ Успех: { success: true }
 * ✅ Ошибка токена: { success: true } (клиент всё равно очистит куки)
 */

import { eventHandler, getCookie } from 'h3'
import { endSession } from '../../utils/sessions'
import { extractJwt } from '../../utils/cookies' // ✅ Централизованный хелпер парсинга куки

export default eventHandler(async (event) => {
  try {
    // ============================================
    // 1. ИЗВЛЕЧЕНИЕ ТОКЕНА И SESSION_ID
    // ============================================
    // ✅ Используем централизованный хелпер (поддержка старого и нового формата куки)
    const token = extractJwt(event)
    const sessionId = getCookie(event, 'session_id')

    // ============================================
    // 2. ПРОВЕРКА: НЕТ ТОКЕНА = УЖЕ ВЫШЕЛ ИЛИ КУКА ПОВРЕЖДЕНА
    // ============================================
    // Не ломаем флоу — клиент всё равно очистит локальное состояние
    if (!token) {
      console.log('[Auth/Logout] ℹ️ Токен отсутствует, пропускаем завершение сессии')
      return { success: true }
    }

    // ============================================
    // 3. ЗАВЕРШЕНИЕ СЕРВЕРНОЙ СЕССИИ
    // ============================================
    if (sessionId) {
      console.log(`[Auth/Logout] 🔄 Завершение сессии: ${sessionId}`)
      await endSession(sessionId)
      console.log(`[Auth/Logout] ✅ Сессия ${sessionId} завершена`)
    } else {
      console.log('[Auth/Logout] ⚠️ sessionId не найден в куках')
    }

    // ============================================
    // 4. ФОРМИРОВАНИЕ ОТВЕТА
    // ============================================
    console.log('[Auth/Logout] ✅ Выход выполнен успешно')
    return { success: true }
    
  } catch (error) {
    // ============================================
    // 5. ОБРАБОТКА ОШИБОК
    // ============================================
    // Логируем ошибку на сервере для отладки
    console.error('[Auth/Logout] ❌ Ошибка при выходе:', error)
    
    // ✅ Возвращаем успех даже при ошибке — это критично!
    // Клиент (authStore) в любом случае очистит куки и перенаправит на /login
    // Если вернуть ошибку — пользователь может "застрять" в авторизованном состоянии
    return { success: true }
  }
})
